
import torch
import torch.nn as nn
import numpy as np
import torch.nn.functional as F

class PatchResBlock(nn.Module):
    def __init__(self, in_channels, out_channels):
        super(PatchResBlock, self).__init__()
        intermediate_dim = int(in_channels / 4)
        self.conv1 = nn.Conv2d(in_channels, intermediate_dim, kernel_size=1, padding=0)
        self.bn1 = nn.BatchNorm2d(intermediate_dim)
        self.relu1 = nn.ReLU(inplace=True)
        self.conv2 = nn.Conv2d(intermediate_dim, intermediate_dim, kernel_size=3, padding=1)
        self.bn2 = nn.BatchNorm2d(intermediate_dim)
        self.relu2 = nn.ReLU(inplace=True)
        self.conv3 = nn.Conv2d(intermediate_dim, out_channels, kernel_size=1, padding=0)
        self.bn3 = nn.BatchNorm2d(out_channels)
        self.conv4 = nn.Conv2d(in_channels, out_channels, kernel_size=1, padding=0)
        self.bn4 = nn.BatchNorm2d(out_channels)
        self.relu = nn.ReLU(inplace=True)
    
    def forward(self, x):
        out = self.relu1(self.bn1(self.conv1(x)))
        out = self.relu2(self.bn2(self.conv2(out)))
        out = self.bn3(self.conv3(out))
        x = self.bn4(self.conv4(x))
        out = x + out
        out = self.relu(out)
        return out
    
